/*
|--------------------------------------------------------------------------
| تنفيذ جميع الأكواد بعد تحميل الصفحة بالكامل
|--------------------------------------------------------------------------
|
| ننتظر حتى ينتهي المتصفح من تحميل جميع عناصر الصفحة،
| وبعدها نبدأ بتنفيذ أوامر JavaScript.
|
*/

document.addEventListener("DOMContentLoaded", function () {

    /*
    |--------------------------------------------------------------------------
    | جلب عناصر نافذة إضافة المقرر
    |--------------------------------------------------------------------------
    |
    | يتم حفظ العناصر داخل متغيرات حتى نستطيع التحكم بها.
    |
    */

    const addModal = document.getElementById("addCourseModal");

    const openAddModal = document.getElementById("openAddModal");

    const closeAddModal = document.getElementById("closeAddModal");



    /*
    |--------------------------------------------------------------------------
    | جلب عناصر نافذة تعديل المقرر
    |--------------------------------------------------------------------------
    */

    const editModal = document.getElementById("editCourseModal");

    const closeEditModal = document.getElementById("closeEditModal");

    const editForm = document.getElementById("editCourseForm");



    /*
    |--------------------------------------------------------------------------
    | فتح نافذة إضافة مقرر
    |--------------------------------------------------------------------------
    |
    | عند الضغط على زر "إضافة مقرر"
    | يتم إظهار الـ Modal.
    |
    */

    openAddModal.addEventListener(

        "click",

        function () {

            addModal.style.display = "flex";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق نافذة إضافة مقرر
    |--------------------------------------------------------------------------
    |
    | عند الضغط على علامة ×
    | يتم إخفاء النافذة.
    |
    */

    closeAddModal.addEventListener(

        "click",

        function () {

            addModal.style.display = "none";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق نافذة تعديل المقرر
    |--------------------------------------------------------------------------
    */

    closeEditModal.addEventListener(

        "click",

        function () {

            editModal.style.display = "none";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق أي نافذة عند الضغط خارجها
    |--------------------------------------------------------------------------
    |
    | إذا ضغط المستخدم على الخلفية السوداء
    | يتم إغلاق النافذة تلقائياً.
    |
    */

    window.addEventListener(

        "click",

        function (event) {

            if (event.target == addModal) {

                addModal.style.display = "none";

            }

            if (event.target == editModal) {

                editModal.style.display = "none";

            }

        }

    );
        /*
    |--------------------------------------------------------------------------
    | فتح نافذة تعديل المقرر
    |--------------------------------------------------------------------------
    |
    | عند الضغط على زر "تعديل"
    | يتم تعبئة جميع الحقول ببيانات المقرر
    | ثم يتم فتح نافذة التعديل.
    |
    */

    document.querySelectorAll(".btn-edit").forEach(function (button) {

        button.addEventListener("click", function () {

            /*
            |--------------------------------------------------------------------------
            | تغيير رابط نموذج التعديل
            |--------------------------------------------------------------------------
            |
            | يتم وضع رقم المقرر داخل رابط التعديل
            | مثال:
            | /courses/5/update
            |
            */

            editForm.action =
                "/courses/" +
                this.dataset.id +
                "/update";


            /*
            |--------------------------------------------------------------------------
            | تعبئة اسم المقرر
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_course_name").value =
                this.dataset.name;


            /*
            |--------------------------------------------------------------------------
            | تعبئة المستوى
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_level").value =
                this.dataset.level;


            /*
            |--------------------------------------------------------------------------
            | تعبئة الفصل الدراسي
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_semester").value =
                this.dataset.semester;


            /*
            |--------------------------------------------------------------------------
            | تعبئة حالة المقرر
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_status").value =
                this.dataset.status;


            /*
            |--------------------------------------------------------------------------
            | إزالة جميع علامات الاختيار القديمة
            |--------------------------------------------------------------------------
            */

            document.querySelectorAll(".edit-department").forEach(function (checkbox) {

                checkbox.checked = false;

            });


            /*
            |--------------------------------------------------------------------------
            | تحديد الأقسام الخاصة بالمقرر
            |--------------------------------------------------------------------------
            |
            | يتم تحويل البيانات القادمة من الزر
            | من نص إلى مصفوفة.
            |
            */

            let departments = this.dataset.departments.split(",");


            /*
            |--------------------------------------------------------------------------
            | وضع علامة صح على الأقسام المرتبطة بالمقرر
            |--------------------------------------------------------------------------
            */

            document.querySelectorAll(".edit-department").forEach(function (checkbox) {

                if (departments.includes(checkbox.value)) {

                    checkbox.checked = true;

                }

            });


            /*
            |--------------------------------------------------------------------------
            | فتح نافذة التعديل
            |--------------------------------------------------------------------------
            */

            editModal.style.display = "flex";

        });

    });


}); // نهاية تحميل الصفحة