/*
|--------------------------------------------------------------------------
| تنفيذ جميع الأكواد بعد تحميل الصفحة
|--------------------------------------------------------------------------
*/

document.addEventListener("DOMContentLoaded", function () {

    /*
    |--------------------------------------------------------------------------
    | عناصر نافذة الإضافة
    |--------------------------------------------------------------------------
    */

    const addModal = document.getElementById("addDepartmentModal");

    const openAddModal = document.getElementById("openAddModal");

    const closeAddModal = document.getElementById("closeAddModal");



    /*
    |--------------------------------------------------------------------------
    | عناصر نافذة التعديل
    |--------------------------------------------------------------------------
    */

    const editModal = document.getElementById("editDepartmentModal");

    const closeEditModal = document.getElementById("closeEditModal");

    const editForm = document.getElementById("editDepartmentForm");



    /*
    |--------------------------------------------------------------------------
    | فتح نافذة الإضافة
    |--------------------------------------------------------------------------
    */

    openAddModal.addEventListener(

        "click",

        function () {

            addModal.style.display = "flex";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق نافذة الإضافة
    |--------------------------------------------------------------------------
    */

    closeAddModal.addEventListener(

        "click",

        function () {

            addModal.style.display = "none";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق نافذة التعديل
    |--------------------------------------------------------------------------
    */

    closeEditModal.addEventListener(

        "click",

        function () {

            editModal.style.display = "none";

        }

    );



    /*
    |--------------------------------------------------------------------------
    | إغلاق النافذة عند الضغط خارجها
    |--------------------------------------------------------------------------
    */

    window.addEventListener(

        "click",

        function (event) {

            if (event.target == addModal) {

                addModal.style.display = "none";

            }

            if (event.target == editModal) {

                editModal.style.display = "none";

            }

        }

    );



    /*
    |--------------------------------------------------------------------------
    | فتح نافذة تعديل القسم
    |--------------------------------------------------------------------------
    */

    document.querySelectorAll(".btn-edit").forEach(function (button) {

        button.addEventListener("click", function () {

            /*
            |--------------------------------------------------------------------------
            | تغيير رابط التعديل
            |--------------------------------------------------------------------------
            */

            editForm.action =

                "/departments/" +

                this.dataset.id +

                "/update";



            /*
            |--------------------------------------------------------------------------
            | تعبئة اسم القسم
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_department_name").value =

                this.dataset.name;



            /*
            |--------------------------------------------------------------------------
            | تعبئة الكلية
            |--------------------------------------------------------------------------
            */

            document.getElementById("edit_college_id").value =

                this.dataset.college;



            /*
            |--------------------------------------------------------------------------
            | فتح نافذة التعديل
            |--------------------------------------------------------------------------
            */

            editModal.style.display = "flex";

        });

    });

});