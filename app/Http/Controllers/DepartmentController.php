<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

use App\Models\Department;
use App\Models\College;
use App\Models\Student;
use App\Models\Employee;

class DepartmentController extends Controller
{

    /*
    |--------------------------------------------------------------------------
    | عرض صفحة إدارة الأقسام
    |--------------------------------------------------------------------------
    */

    public function index(Request $request)
    {

        /*
        |--------------------------------------------------------------------------
        | إنشاء استعلام الأقسام
        |--------------------------------------------------------------------------
        */

        $departments = Department::with('college');

        /*
        |--------------------------------------------------------------------------
        | البحث باسم القسم
        |--------------------------------------------------------------------------
        */

        if($request->search)
        {

            $departments->where(

                'department_name',

                'like',

                '%' . $request->search . '%'

            );

        }

        /*
        |--------------------------------------------------------------------------
        | تنفيذ الاستعلام
        |--------------------------------------------------------------------------
        */

        $departments = $departments->get();

        /*
        |--------------------------------------------------------------------------
        | جلب الكليات
        |--------------------------------------------------------------------------
        */

        $colleges = College::all();

        /*
        |--------------------------------------------------------------------------
        | عدد الطلاب
        |--------------------------------------------------------------------------
        */

        $students = Student::all();

        /*
        |--------------------------------------------------------------------------
        | عدد الموظفين
        |--------------------------------------------------------------------------
        */

        $employees = Employee::all();

        /*
        |--------------------------------------------------------------------------
        | إرسال البيانات
        |--------------------------------------------------------------------------
        */

        return view(

            'departments.index',

            compact(

                'departments',

                'colleges',

                'students',

                'employees'

            )

        );

    }



    /*
    |--------------------------------------------------------------------------
    | إضافة قسم جديد
    |--------------------------------------------------------------------------
    */

    public function store(Request $request)
    {

        /*
        |--------------------------------------------------------------------------
        | التحقق من البيانات
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'department_name' =>

            'required|string|max:255|unique:departments,department_name',

            'college_id' =>

            'required|exists:colleges,id'

        ]);



        /*
        |--------------------------------------------------------------------------
        | حفظ القسم
        |--------------------------------------------------------------------------
        */

        Department::create([

            'department_name' =>

            $request->department_name,

            'college_id' =>

            $request->college_id

        ]);



        /*
        |--------------------------------------------------------------------------
        | رسالة النجاح
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->back()

            ->with(

                'success',

                'تم إضافة القسم بنجاح.'

            );

    }



    /*
    |--------------------------------------------------------------------------
    | تعديل قسم
    |--------------------------------------------------------------------------
    */

    public function update(Request $request,$id)
    {

        /*
        |--------------------------------------------------------------------------
        | التحقق
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'department_name' =>

            'required|string|max:255|unique:departments,department_name,'.$id,

            'college_id' =>

            'required|exists:colleges,id'

        ]);



        /*
        |--------------------------------------------------------------------------
        | البحث عن القسم
        |--------------------------------------------------------------------------
        */

        $department = Department::findOrFail($id);



        /*
        |--------------------------------------------------------------------------
        | تعديل البيانات
        |--------------------------------------------------------------------------
        */

        $department->update([

            'department_name' =>

            $request->department_name,

            'college_id' =>

            $request->college_id

        ]);



        /*
        |--------------------------------------------------------------------------
        | رسالة النجاح
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->back()

            ->with(

                'success',

                'تم تعديل القسم بنجاح.'

            );

    }



    /*
    |--------------------------------------------------------------------------
    | حذف قسم
    |--------------------------------------------------------------------------
    */

    public function destroy($id)
    {

        /*
        |--------------------------------------------------------------------------
        | البحث عن القسم
        |--------------------------------------------------------------------------
        */

        $department = Department::with(

            'students',

            'courses'

        )->findOrFail($id);



        /*
        |--------------------------------------------------------------------------
        | منع الحذف إذا كان القسم مستخدماً
        |--------------------------------------------------------------------------
        */

        if(

            $department->students->count() > 0 ||

            $department->courses->count() > 0

        )
        {

            return redirect()

                ->back()

                ->with(

                    'error',

                    'لا يمكن حذف القسم لأنه مرتبط بطلاب أو مقررات.'

                );

        }



        /*
        |--------------------------------------------------------------------------
        | حذف القسم
        |--------------------------------------------------------------------------
        */

        $department->delete();



        /*
        |--------------------------------------------------------------------------
        | رسالة النجاح
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->back()

            ->with(

                'success',

                'تم حذف القسم بنجاح.'

            );

    }

}