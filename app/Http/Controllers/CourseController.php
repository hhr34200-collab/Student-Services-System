<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

use App\Models\Course;

use App\Models\Department;

class CourseController extends Controller
{

    /*
    |--------------------------------------------------------------------------
    | عرض صفحة إدارة المقررات
    |--------------------------------------------------------------------------
    |
    | تقوم هذه الدالة بجلب جميع المقررات مع الأقسام المرتبطة بها.
    | كما تقوم بجلب جميع الأقسام حتى تظهر داخل الـ Modal
    | عند إضافة أو تعديل مقرر.
    |
    */

    public function index(Request $request)
    {

        /*
        |--------------------------------------------------------------------------
        | إنشاء استعلام المقررات
        |--------------------------------------------------------------------------
        */

        $courses = Course::with('departments');

        /*
        |--------------------------------------------------------------------------
        | إذا كتب المدير كلمة بحث
        |--------------------------------------------------------------------------
        */

        if ($request->search) {

            $courses->where(

                'course_name',

                'like',

                '%' . $request->search . '%'

            );

        }

        /*
        |--------------------------------------------------------------------------
        | تنفيذ الاستعلام
        |--------------------------------------------------------------------------
        */

        $courses = $courses->get();

        /*
        |--------------------------------------------------------------------------
        | جلب جميع الأقسام
        |--------------------------------------------------------------------------
        */

        $departments = Department::all();

        /*
        |--------------------------------------------------------------------------
        | إرسال البيانات إلى الصفحة
        |--------------------------------------------------------------------------
        */

        return view(

            'courses.index',

            compact(

                'courses',

                'departments'

            )

        );

    }
    /*
|--------------------------------------------------------------------------
| إضافة مقرر جديد
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بحفظ بيانات المقرر أولاً داخل جدول courses،
| ثم تقوم بربطه بالأقسام المختارة داخل جدول course_departments.
|
*/

public function store(Request $request)
{
 
    /*
    |--------------------------------------------------------------------------
    | التحقق من صحة البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'course_name' => 'required|string|max:255',

        'level' => 'required',

        'semester' => 'required',

        'status' => 'required',

        'departments' => 'required|array',

       'departments.*' => 'exists:departments,id'

    ]);



    /*
    |--------------------------------------------------------------------------
    | حفظ بيانات المقرر
    |--------------------------------------------------------------------------
    */

    $course = Course::create([

        'course_name' => $request->course_name,

        'level' => $request->level,

        'semester' => $request->semester,

        'status' => $request->status

    ]);



    /*
    |--------------------------------------------------------------------------
    | ربط المقرر بالأقسام المختارة
    |--------------------------------------------------------------------------
    |
    | هنا نستعمل belongsToMany().
    | تقوم attach() بإضافة سجل داخل
    | جدول course_departments لكل قسم تم اختياره.
    |
    */

    $course->departments()->attach(

        $request->departments

    );



    /*
    |--------------------------------------------------------------------------
    | الرجوع برسالة نجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم إضافة المقرر بنجاح.'

        );

}
/*
|--------------------------------------------------------------------------
| تعديل بيانات المقرر
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بتعديل بيانات المقرر،
| ثم تحديث الأقسام المرتبطة به.
|
*/

public function update(Request $request, $id)
{

    /*
    |--------------------------------------------------------------------------
    | التحقق من صحة البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'course_name' => 'required|string|max:255',

        'level' => 'required',

        'semester' => 'required',

        'status' => 'required',

        'departments' => 'required|array',
        'departments.*' => 'exists:departments,id'

    ]);


    /*
    |--------------------------------------------------------------------------
    | جلب المقرر
    |--------------------------------------------------------------------------
    */

  $course = Course::where(

    'course_id',

    $id

)->firstOrFail();


    /*
    |--------------------------------------------------------------------------
    | تعديل بيانات المقرر
    |--------------------------------------------------------------------------
    */

    $course->update([

        'course_name' => $request->course_name,

        'level' => $request->level,

        'semester' => $request->semester,

        'status' => $request->status

    ]);


    /*
    |--------------------------------------------------------------------------
    | تحديث الأقسام المرتبطة بالمقرر
    |--------------------------------------------------------------------------
    |
    | تقوم sync() بحذف العلاقات القديمة
    | ثم إنشاء العلاقات الجديدة تلقائياً.
    |
    */

    $course->departments()->sync(

        $request->departments

    );


    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم تعديل المقرر بنجاح'

        );

}
/*
|--------------------------------------------------------------------------
| حذف مقرر
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بحذف المقرر.
| وبسبب cascadeOnDelete سيتم حذف جميع العلاقات الموجودة
| داخل جدول course_departments تلقائياً.
|
*/

public function destroy($id)
{

    /*
    |--------------------------------------------------------------------------
    | البحث عن المقرر
    |--------------------------------------------------------------------------
    */

  $course = Course::where(

    'course_id',

    $id

)->firstOrFail();

    /*
    |--------------------------------------------------------------------------
    | حذف المقرر
    |--------------------------------------------------------------------------
    */

    $course->delete();


    /*
    |--------------------------------------------------------------------------
    | الرجوع برسالة نجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم حذف المقرر بنجاح.'

        );

}
/*
|--------------------------------------------------------------------------
| تغيير حالة المقرر
|--------------------------------------------------------------------------
|
| إذا كان المقرر نشطاً يصبح غير نشط،
| وإذا كان غير نشط يصبح نشطاً.
|
*/

public function toggleStatus($id)
{

    /*
    |--------------------------------------------------------------------------
    | البحث عن المقرر
    |--------------------------------------------------------------------------
    */
$course = Course::where(

    'course_id',

    $id

)->firstOrFail();


    /*
    |--------------------------------------------------------------------------
    | تغيير الحالة
    |--------------------------------------------------------------------------
    */

    if($course->status == 'active')
    {

        $course->status = 'inactive';

    }
    else
    {

        $course->status = 'active';

    }


    /*
    |--------------------------------------------------------------------------
    | حفظ التعديل
    |--------------------------------------------------------------------------
    */

    $course->save();


    /*
    |--------------------------------------------------------------------------
    | الرجوع برسالة نجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم تغيير حالة المقرر بنجاح.'

        );

}

}