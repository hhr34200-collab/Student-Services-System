<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

use App\Models\CourseAssignment;
use App\Models\Course;
use App\Models\Department;
use App\Models\Employee;

class CourseAssignmentController extends Controller
{

    /*
    |--------------------------------------------------------------------------
    | عرض صفحة توزيع المقررات
    |--------------------------------------------------------------------------
    |
    | تقوم هذه الدالة بجلب جميع بيانات التوزيع مع المقرر والقسم
    | والموظف حتى تظهر داخل الجدول.
    |
    */

    public function index(Request $request)
    {

        /*
        |--------------------------------------------------------------------------
        | إنشاء استعلام التوزيع
        |--------------------------------------------------------------------------
        */

        $assignments = CourseAssignment::with(

            'course',

            'department',

            'employee'

        );



        /*
        |--------------------------------------------------------------------------
        | البحث باسم المقرر
        |--------------------------------------------------------------------------
        */

        if($request->search)
        {

            $assignments->whereHas(

                'course',

                function($query) use ($request){

                    $query->where(

                        'course_name',

                        'like',

                        '%'.$request->search.'%'

                    );

                }

            );

        }



        /*
        |--------------------------------------------------------------------------
        | تنفيذ الاستعلام
        |--------------------------------------------------------------------------
        */

        $assignments = $assignments->get();



        /*
        |--------------------------------------------------------------------------
        | جلب المقررات
        |--------------------------------------------------------------------------
        */

        $courses = Course::all();



        /*
        |--------------------------------------------------------------------------
        | جلب الأقسام
        |--------------------------------------------------------------------------
        */

        $departments = Department::all();



        /*
        |--------------------------------------------------------------------------
        | جلب الموظفين
        |--------------------------------------------------------------------------
        */

        $employees = Employee::all();



        /*
        |--------------------------------------------------------------------------
        | إرسال البيانات إلى الصفحة
        |--------------------------------------------------------------------------
        */

        return view(

            'course_assignments.index',

            compact(

                'assignments',

                'courses',

                'departments',

                'employees'

            )

        );
        

    }
    /*
|--------------------------------------------------------------------------
| إضافة توزيع جديد
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بحفظ توزيع المقرر على عضو هيئة التدريس.
| يتم الحفظ داخل جدول course_assignments فقط.
|
*/

public function store(Request $request)
{

    /*
    |--------------------------------------------------------------------------
    | التحقق من صحة البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'course_id' =>

        'required|exists:courses,course_id',

        'department_id' =>

        'required|exists:departments,id',

        'employee_id' =>

        'required|exists:employees,employee_id',

        'academic_year' =>

        'required|string|max:255',

        'semester' =>

        'required|in:first,second'

    ]);



    /*
    |--------------------------------------------------------------------------
    | منع تكرار نفس التوزيع
    |--------------------------------------------------------------------------
    */

    $exists = CourseAssignment::where(

        'course_id',

        $request->course_id

    )

    ->where(

        'department_id',

        $request->department_id

    )

    ->where(

        'employee_id',

        $request->employee_id

    )

    ->where(

        'academic_year',

        $request->academic_year

    )

    ->where(

        'semester',

        $request->semester

    )

    ->exists();



    /*
    |--------------------------------------------------------------------------
    | إذا كان التوزيع موجوداً
    |--------------------------------------------------------------------------
    */

    if($exists)
    {

        return redirect()

            ->back()

            ->with(

                'error',

                'هذا التوزيع موجود مسبقاً.'

            );

    }



    /*
    |--------------------------------------------------------------------------
    | حفظ التوزيع
    |--------------------------------------------------------------------------
    */

    CourseAssignment::create([

        'course_id' =>

        $request->course_id,

        'department_id' =>

        $request->department_id,

        'employee_id' =>

        $request->employee_id,

        'academic_year' =>

        $request->academic_year,

        'semester' =>

        $request->semester

    ]);



    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم توزيع المقرر بنجاح.'

        );

}
/*
|--------------------------------------------------------------------------
| تعديل توزيع مقرر
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بتعديل بيانات التوزيع.
|
*/

public function update(Request $request, $id)
{

    /*
    |--------------------------------------------------------------------------
    | التحقق من صحة البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'course_id' =>

        'required|exists:courses,course_id',

        'department_id' =>

        'required|exists:departments,id',

        'employee_id' =>

        'required|exists:employees,employee_id',

        'academic_year' =>

        'required|string|max:255',

        'semester' =>

        'required|in:first,second'

    ]);



    /*
    |--------------------------------------------------------------------------
    | البحث عن التوزيع
    |--------------------------------------------------------------------------
    */

    $assignment = CourseAssignment::findOrFail($id);



    /*
    |--------------------------------------------------------------------------
    | تحديث البيانات
    |--------------------------------------------------------------------------
    */

    $assignment->update([

        'course_id' =>

        $request->course_id,

        'department_id' =>

        $request->department_id,

        'employee_id' =>

        $request->employee_id,

        'academic_year' =>

        $request->academic_year,

        'semester' =>

        $request->semester

    ]);



    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم تعديل توزيع المقرر بنجاح.'

        );

}




/*
|--------------------------------------------------------------------------
| حذف توزيع مقرر
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بحذف سجل التوزيع فقط،
| ولا تحذف المقرر أو الموظف أو القسم.
|
*/

public function destroy($id)
{

    /*
    |--------------------------------------------------------------------------
    | البحث عن التوزيع
    |--------------------------------------------------------------------------
    */

    $assignment = CourseAssignment::findOrFail($id);



    /*
    |--------------------------------------------------------------------------
    | حذف سجل التوزيع
    |--------------------------------------------------------------------------
    */

    $assignment->delete();



    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم حذف توزيع المقرر بنجاح.'

        );

}
}