<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

use App\Models\College;
use App\Models\Department;

class CollegeController extends Controller
{

   public function index(Request $request)
{
    /*
    |--------------------------------------------------------------------------
    | إنشاء استعلام الكليات
    |--------------------------------------------------------------------------
    */

    $colleges = College::with('departments');

    /*
    |--------------------------------------------------------------------------
    | البحث باسم الكلية
    |--------------------------------------------------------------------------
    */

    if($request->search)
    {
        $colleges->where(

            'college_name',

            'like',

            '%' . $request->search . '%'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | تنفيذ الاستعلام
    |--------------------------------------------------------------------------
    */

    $colleges = $colleges->get();

    /*
    |--------------------------------------------------------------------------
    | جلب الأقسام للإحصائيات
    |--------------------------------------------------------------------------
    */

    $departments = Department::all();

    /*
    |--------------------------------------------------------------------------
    | إرسال البيانات
    |--------------------------------------------------------------------------
    */

    return view(

        'colleges.index',

        compact(

            'colleges',

            'departments'

        )

    );
}
   
    /*
|--------------------------------------------------------------------------
| إضافة مقرر جديد
|--------------------------------------------------------------------------
|
| تقوم هذه الدالة بحفظ بيانات المقرر أولاً داخل جدول courses،
| ثم تقوم بربطه بالأقسام المختارة داخل جدول course_departments.
|
*/

public function store(Request $request)
{

    /*
    |--------------------------------------------------------------------------
    | التحقق من البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'college_name'=>'required|string|max:255',

        
    ]);



    /*
    |--------------------------------------------------------------------------
    | حفظ الكلية
    |--------------------------------------------------------------------------
    */

    College::create([

        'college_name'=>$request->college_name,

       

    ]);



    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

    ->back()

    ->with(

        'success',

        'تم إضافة الكلية بنجاح.'

    );

}

/*
|--------------------------------------------------------------------------
| تعديل بيانات الكلية
|--------------------------------------------------------------------------
*/

public function update(Request $request, $id)
{
    /*
    |--------------------------------------------------------------------------
    | التحقق من صحة البيانات
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'college_name' => 'required|string|max:255',

        

    ]);


    /*
    |--------------------------------------------------------------------------
    | البحث عن الكلية
    |--------------------------------------------------------------------------
    */

    $college = College::findOrFail($id);


    /*
    |--------------------------------------------------------------------------
    | تعديل البيانات
    |--------------------------------------------------------------------------
    */

    $college->update([

        'college_name' => $request->college_name,

        

    ]);


    /*
    |--------------------------------------------------------------------------
    | رسالة نجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم تعديل الكلية بنجاح.'

        );

}


/*
|--------------------------------------------------------------------------
| حذف كلية
|--------------------------------------------------------------------------
|
| لا يسمح بحذف الكلية إذا كانت تحتوي على أقسام.
|
*/

public function destroy($id)
{

    /*
    |--------------------------------------------------------------------------
    | البحث عن الكلية
    |--------------------------------------------------------------------------
    */

    $college = College::with('departments')->findOrFail($id);


    /*
    |--------------------------------------------------------------------------
    | التحقق من وجود أقسام
    |--------------------------------------------------------------------------
    */

    if($college->departments->count() > 0)
    {

        return redirect()

            ->back()

            ->with(

                'error',

                'لا يمكن حذف الكلية لأنها تحتوي على أقسام.'

            );

    }


    /*
    |--------------------------------------------------------------------------
    | حذف الكلية
    |--------------------------------------------------------------------------
    */

    $college->delete();


    /*
    |--------------------------------------------------------------------------
    | رسالة النجاح
    |--------------------------------------------------------------------------
    */

    return redirect()

        ->back()

        ->with(

            'success',

            'تم حذف الكلية بنجاح.'

        );

}


